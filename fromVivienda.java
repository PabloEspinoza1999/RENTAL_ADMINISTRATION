    public void Actualizar(){
         
         
            String M[][] = new String[MenuPrinc.listaVivienda.size()][14];
            
            for (int i = 0; i < MenuPrinc.listaVivienda.size(); i++) {
             Viviendas Aux =  (Viviendas) MenuPrinc.listaVivienda.get(i);
             
             M[i][0]= Integer.toString(Aux.getIdviviendas());
             M[i][1]=Aux.getDescripcion();
             M[i][2]=Aux.getDireccion();
             M[i][3]=Integer.toString(Aux.getMtsConstrucc());
             M[i][4]=Integer.toString(Aux.getMtsLote());
             M[i][5]=Aux.getTipoConstruccion();
             M[i][6]=Aux.getCochera().toString();
             M[i][7]=Integer.toString(Aux.getCantHabitacion());
             M[i][8]=Integer.toString(Aux.getCantbanos());
             M[i][9]=Aux.getCarretera();
             M[i][10]=Integer.toString(Aux.getPrecioBase());
             M[i][11]=Integer.toString(Aux.getDepositoGarantia());
             M[i][12]=Aux.getPropetarios();
             M[i][13]=Aux.getEstado();
                     
            }
           
            
            
            Tabla.setModel(new javax.swing.table.DefaultTableModel(
            M,
            new String [] {
                "ID", "Descripción", "Dirrección", "M.Contruc", "M.Lote", "T.Construc", "Cochera",
                "C. Habitac", "C. baños", "Carretera", "Precio Base", "Deposito.G", "Propetario", "Estado"
            }
        ));
            
           
            //
          
      
     }
    
    
    
    
    
    private void SeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SeleccionarActionPerformed

      posi = Tabla.getSelectedRow();
     Viviendas Aux =  (Viviendas) MenuPrinc.listaVivienda.get(posi);
        try {
     CantBanos.setText(Integer.toString(Aux.getCantbanos()));
     CantHabi.setText(Integer.toString(Aux.getCantHabitacion()));
     Caterra.setSelectedItem(Aux.getCarretera());
     Cochera.setSelectedItem(Aux.getCochera());
     DGarantia.setText(Integer.toString(Aux.getDepositoGarantia()));
     MConstru.setText(Integer.toString(Aux.getMtsConstrucc()));
     MLote.setText(Integer.toString(Aux.getMtsLote()));
     PrecioBase.setText(Integer.toString(Aux.getPrecioBase()));
     Propieta.setText(Aux.getPropetarios());
     TContruc.setSelectedItem(Aux.getTipoConstruccion());
     txtDescrip.setText(Aux.getDescripcion());
     txtDirec.setText(Aux.getDireccion());
     txtEstado.setSelectedItem(Aux.getEstado());
     txtID.setText(Integer.toString(Aux.getIdviviendas()));
            
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null,"SELECCIONE UNA FILA");
        }

    }//GEN-LAST:event_SeleccionarActionPerformed

    private void BnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BnBuscarActionPerformed
         Boolean Existencia = false;
     if(!txtBuscar.getText().isEmpty()){
               Viviendas Aux = null;
              String str = txtBuscar.getText();
          for (int i = 0; i < MenuPrinc.listaVivienda.size(); i++) {
              Aux = (Viviendas) MenuPrinc.listaVivienda.get(i);
              if(Integer.toString(Aux.getIdviviendas()).equals(str)){
                mostrarDatos(Aux); 
                Existencia=true;
           }
            
        }
        
    }else{
        
        JOptionPane.showMessageDialog(null,"La casilla Buscar esta vacia");
    }
   
        
        if(Existencia){
        JOptionPane.showMessageDialog(null,"Busqueda finalizada");
        }else{
         JOptionPane.showMessageDialog(null,"Lo sentimos no se ha encontrado la información en la base de datos :(");    
        }
        
         txtBuscar.setText("");
    }//GEN-LAST:event_BnBuscarActionPerformed

    
