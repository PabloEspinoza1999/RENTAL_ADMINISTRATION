    private void InsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertarActionPerformed
       
          ///Este "if" realiza una revisión que todo los textfields esten llenos
        if(   !CantBanos.getText().equals("")   &&  !CantHabi.getText().equals("")   &&
             !DGarantia.getText().equals("")   &&  !MConstru.getText().equals("")   &&
             !MLote.getText().equals("")       &&  !PrecioBase.getText().equals("") &&
             !Propieta.getText().equals("")    &&  !txtDescrip.getText().equals("") &&
             !txtDirec.getText().equals("")    &&  !txtID.getText().equals("")   ){     
         
           
        //// Este "for" realiza una busqueda para  comprobar si el identicador ingresado Existe 
                 Boolean Existencia1=true;
                 String str = txtID.getText();
                 for (int i = 0; i < MenuPrinc.listaVivienda.size(); i++) {
                      Viviendas Aux =  (Viviendas) MenuPrinc.listaVivienda.get(i);
                     if(Integer.toString(Aux.getIdviviendas()).equals(str)){
                           Existencia1=false;
                      }    
                  }
         
                if(Existencia1){
                
                   ///Enviando datos al contructor
                   Viviendas  NuevaVivienda = new Viviendas(Integer.parseInt(txtID.getText()),txtDescrip.getText(),
                   txtDirec.getText(),Integer.parseInt(MConstru.getText()),Integer.parseInt(MLote.getText()),
                   (String) TContruc.getSelectedItem(),true,Integer.parseInt(CantHabi.getText()),
                    Integer.parseInt(CantBanos.getText()), (String) Caterra.getSelectedItem(),
                    Integer.parseInt(PrecioBase.getText()),Integer.parseInt(DGarantia.getText()),
                    Propieta.getText(),(String)txtEstado.getSelectedItem());
                    MenuPrinc.listaVivienda.add(NuevaVivienda);
  
                        Actualizar();  //Muestra el Contenido en JTable
                        Limpiar(); //Limpia los Jtable 
                 }else{
                   JOptionPane.showMessageDialog(null,"El Identificador :"+txtID.getText()+" Ya  existe , Por favor ingrese un nuevo valor");
                 }
                 
                 
                 
         }else{
              JOptionPane.showMessageDialog(null,"Hay Una casilla vacia");
                     
         }
    
    }//GEN-LAST:event_InsertarActionPerformed
     
   
    
