    private void BnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BnBuscarActionPerformed

       Boolean Existencia = false;
     if(!txtBuscar.getText().isEmpty()){
               Inquilinos Aux = null;
              String str = txtBuscar.getText();
          for (int i = 0; i < MenuPrinc.listaInquilinos.size(); i++) {
              Aux = (Inquilinos) MenuPrinc.listaInquilinos.get(i);
              if(Integer.toString(Aux.getCedInqui()).equals(str)){
                mostrarDatos(Aux); 
                Existencia=true;
           }   
        }
        
    }else{
        
        JOptionPane.showMessageDialog(null,"La casilla Buscar esta vacia");
    }
   
     
     
     
        //Resultados de la Busqueda
        if(Existencia){
        JOptionPane.showMessageDialog(null,"Busqueda finalizada");
        }else{
         JOptionPane.showMessageDialog(null,"Lo sentimos no se ha encontrado la información en la base de datos :(");    
        }
        
         txtBuscar.setText("");

    }//GEN-LAST:event_BnBuscarActionPerformed
   
    public  void mostrarDatos(Inquilinos Aux) {
      
         String M[][] = new String[1][8];
            
             
             M[0][0]= Integer.toString(Aux.getCedInqui());
             M[0][1]=Aux.getNomInqui();
             M[0][2]=Aux.getGenero();
             M[0][3]=Aux.getEmail();
             M[0][4]=Aux.getFechNac();
             M[0][5]=Aux.getText();
             M[0][6]=Aux.getTelefono();
             M[0][7]= Aux.getOcupacion();
             
            VerPersona(M); // Este metodo muestra los datos buscados, en los textfields
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
            M,
            new String [] {
                "Cédula", "Nombre", "Genero", "Email", "Fecha.N", "Dirección", "Teléfono", "Ocupación"
            }
        ));
        
    }
    
    
