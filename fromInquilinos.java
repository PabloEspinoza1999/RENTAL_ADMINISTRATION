    private void InsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertarActionPerformed
     
        
       if(!txtcedula.getText().isEmpty() && !txtNombre.getText().isEmpty() && txtGenero.getSelectedIndex()>0 &&
               !txtFechaNacimiento.getText().isEmpty() && !txtDireccion.getText().isEmpty() && !txtTelefono.getText().isEmpty() 
               && !txtEmial.getText().isEmpty() && !txtOcupacion.getText().isEmpty()){ 
        
           
                 Boolean Existencia1=true;
                 String str = txtcedula.getText();
                 for (int i = 0; i < MenuPrinc.listaInquilinos.size(); i++) {
                      Inquilinos Aux =  (Inquilinos) MenuPrinc.listaInquilinos.get(i);
                     if(Integer.toString(Aux.getCedInqui()).equals(str)){
                           Existencia1=false;
                      }    
                  }
      
           
             if(Existencia1){
                    Inquilinos  persona = new Inquilinos(Integer.parseInt(txtcedula.getText()),txtNombre.getText(),
                   (String) txtGenero.getSelectedItem(),txtFechaNacimiento.getText(),txtDireccion.getText(),
                   txtTelefono.getText(), txtEmial.getText(),txtOcupacion.getText());
              
                   MenuPrinc.listaInquilinos.add(persona);
                   Actualizar();
                   Limpiar();
             }else{
               
               JOptionPane.showMessageDialog(null,"El Identificador :"+txtcedula.getText()+" Ya  existe , Por favor ingrese un nuevo valor");
            } 
            
            
            
       }else{ 
           
             JOptionPane.showMessageDialog(null,"Hay Una casilla vacia");
       }
    

    }//GEN-LAST:event_InsertarActionPerformed

