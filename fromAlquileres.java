    public void  Actualizar(){
          String M[][] = new String[MenuPrinc.listaAlquileres.size()][11];
            
            for (int i = 0; i < MenuPrinc.listaAlquileres.size(); i++) {
            Alquileres  Aux = (Alquileres) MenuPrinc.listaAlquileres.get(i);
           
             M[i][0]=Integer.toString(Aux.getNumAlquiler());
             M[i][1]=Aux.getFechContrato();
             M[i][2]=Integer.toString(Aux.getCantMeses());
             M[i][3]=Integer.toString(Aux.getNumAdultos());
             M[i][4]=Integer.toString(Aux.getNumNinos());
             M[i][5]=Integer.toString(Aux.getDepositoGarantia());
             M[i][6]=Integer.toString(Aux.getPrecioAlquiler());
             M[i][7]=Integer.toString(Aux.getPorcincremAnual());
             M[i][8]=Integer.toString(Aux.getCedInquilino());
             M[i][9]=Integer.toString(Aux.getIdVvienda());
             M[i][10]=Aux.getEstado();
            }
              
          jTable2.setModel(new javax.swing.table.DefaultTableModel(
            M,
            new String [] {
                "Numero.Alquiler", "Fecha.contrato", "Cant.Meses", "Numero.Adultos", "Numero.niños", "Deposito.G", 
                "Precio.Alquiler", "Porce.I.A", "Cedula.Inquilino", "ID.Vivienda", "Estado"
            }
        ));
       
       
   }
