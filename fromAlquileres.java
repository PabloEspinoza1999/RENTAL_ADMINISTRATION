    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        
    if(!CantMeses.getText().isEmpty() &&  !FechaCont.getText().isEmpty() &&  
       !txtCedInquilino.getText().isEmpty() && !txtDepositoG.getText().isEmpty() && 
       txtEstado.getSelectedIndex()>0 &&  !txtIDVivi.getText().isEmpty() &&
       !txtNumAdult.getText().isEmpty() && !txtNumAlquiler.getText().isEmpty() &&
       !txtNumNinos.getText().isEmpty()  && !txtPorcenIA.getText().isEmpty() &&
       !txtPrecioAlquiler.getText().isEmpty() ){
           
           
              //// Este "for" realiza una busqueda para  comprobar si el identicador ingresado Existe 
                 Boolean Existencia1=true;
                 String str = txtNumAlquiler.getText();
                 for (int i = 0; i < MenuPrinc.listaAlquileres.size(); i++) {
                   Alquileres  Aux = (Alquileres) MenuPrinc.listaAlquileres.get(i);
                     if(Integer.toString(Aux.getNumAlquiler()).equals(str)){
                           Existencia1=false;
                      }    
                  }
         
                 
             if(Existencia1){
                 
                Alquileres Persona = new Alquileres(Integer.parseInt(txtNumAlquiler.getText()),FechaCont.getText(),
                Integer.parseInt(CantMeses.getText()),Integer.parseInt(txtNumAdult.getText()),
                Integer.parseInt(txtNumNinos.getText()),Integer.parseInt( txtDepositoG.getText()),
                Integer.parseInt( txtPrecioAlquiler.getText()),Integer.parseInt( txtPorcenIA.getText()),
                Integer.parseInt( txtCedInquilino.getText()),Integer.parseInt( txtIDVivi.getText()), (String) txtEstado.getSelectedItem());
             
                MenuPrinc.listaAlquileres.add(Persona);
                Actualizar();
                Limpiar();
             
             }else{
                    JOptionPane.showMessageDialog(null,"El Identificador : "+txtNumAlquiler.getText()+"  Ya  existe , Por favor ingrese un nuevo valor");
             }
            
            
    }else{
       JOptionPane.showMessageDialog(null,"Hay Una casilla vacia");
                     
     }
       
    }//GEN-LAST:event_jButton1ActionPerformed
   
    
